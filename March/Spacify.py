# Spacify

# CodeWars Link - https://www.codewars.com/kata/57f8ee485cae443c4d000127

# Problem Statement
# Modify the spacify function so that it returns the given string with spaces inserted between each character.

# Example
# Input: "Jason"
# Output: "J a s o n"

# My Solution
# Learned that join() accepts strings, making it useful for string maniupluation
def spacify(string):

    return " ".join(string)
