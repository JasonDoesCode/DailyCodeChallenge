# Reversed Strings

# Codewars Link - https://www.codewars.com/users/imJasonLeo/completed_solutions

# Problem Statement
# Complete the solution so that it reverses the string passed into it.

# Examples
# 'world'  =>  'dlrow'
# 'word'   =>  'drow'

# My Solution
# Make use of Slice notation and step backwards through the string
def solution(str):
    return str[::-1]
